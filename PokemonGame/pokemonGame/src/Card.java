public class Card {

    private String name;
  
/**
 * Retrieves the card name.
 *
 * @return the name as a String.
 */

public String getName() {
    return name;
}

/**
 * Sets the card name to the specified value.
 *
 * @param n the name to set, represented as a String.
 */
public void setName(String n) {
    name = n;
}

}

